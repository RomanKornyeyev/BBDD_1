CREATE OR REPLACE PROCEDURE SAL_EMPLE(NUM_EMP NUMBER)
AS 
V_SAL EMPLE.SALARIO%TYPE;
BEGIN 
SELECT SALARIO INTO V_SAL FROM EMPLE WHERE EMP_NO=NUM_EMP;
DBMS_OUTPUT.PUT_LINE('Salario= '||V_SAL);
END;
/

