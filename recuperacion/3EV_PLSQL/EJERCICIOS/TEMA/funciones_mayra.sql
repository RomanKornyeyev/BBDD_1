CREATE OR REPLACE FUNCTION SUMANUM(NUM1 NUMBER, NUM2 NUMBER)
RETURN NUMBER
IS 
	SUMA NUMBER;
BEGIN
	SUMA:=NUM1+NUM2;
	RETURN(SUMA);
END;
/

